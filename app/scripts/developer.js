
class Developer { // eslint-disable-line no-unused-vars
  /**
   * DeveloperTemplate constructor
   * @param {String} id
   * @param {String} name
   * @param {String} title
   * @param {String} bio
   * @param {String} avatar
   * @param {String} web
   * @param {String} twitter
   * @param {String} github
   */
  constructor(id, name, title, bio, avatar, web, twitter, github) {
    this.id = id;
    this.name = name;
    this.title = title;
    this.bio = bio;
    this.avatar = avatar;
    this.web = web;
    this.twitter = twitter;
    this.github = github;
  }
}
